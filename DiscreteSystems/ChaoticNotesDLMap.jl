# ╔═╡ 9aba41cb-de7f-48bc-bbc6-d1dab95bd2f7
md" λ = $λ, n = $n "

# ╔═╡ 4216313f-ebf3-4f9e-884f-c933cc9aa175
f(x, λ) = λ * x * (1-x)

# ╔═╡ ae31376a-9576-4143-bebc-8c367a2451b3
begin
	function ^(f::Function, n)
		function h(x, p)
			z = f(x, p)
			for i in 2:n
				z = f(z, p)
			end
			return z
		end
		return h
	end
	nothing
end


# ╔═╡ 64e23707-86b7-4436-94f0-2e84c7ce5c52
begin
	x = 0:0.001:1
	plot(x, f.(x, λ), background_color = :black, color = :white, xlims = (0, 1), ylims = (0, 1), legend=false, aspect_ratio=:equal)
	plot!(x, x)
	f_n = f^n
	plot!(x, f_n.(x, λ))
end

# ╔═╡ 9cd9d314-c215-4e2b-afbc-9955e340224d
md"""
Great! And now what? It is interesting to note that $\mid f'(3/4) \mid)$ goes from less than to greater than $1$ when $\lambda = 3$. That represents the change in the stability of the fixed point that now becomes unstable (that is why it can't be seen in the orbit diagram below). If we set $n=2$, we can also seen that now $f^2$ crosses the diagonal twice with $\mid (f^2)' \mid < 1$. We just witnessed the creation of a stable *2-cycle* at the cost of the single stable *1-cycle* we had before. By playing around with the plot you can see that this thing called **period doubling bifurcation** keeps on happening over and over.

Let's now see what happens when we set $\lambda = 4$ and let $n$ vary. We can see that the number of fixed points keeps on increasing as n goes up (and it doesn't seem to want to stop either!). A dense number of fixed points is reminiscent of chaotic behaviour.
It would be great to be able to visualize the change in the fixed points of a system as we vary the initial parameters. Fortunately an **orbit diagram** allows us to do just that (more specifically, they show us the *stable* fixed points).

"""

# ╔═╡ dff70b94-774f-46cd-b411-3ee9739a9aca
