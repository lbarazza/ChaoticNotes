## Discrete Dynamical Systems

A discrete dynamical system is a system whose evolution is described by a law of the type

$$x_{n+1} = f(x_n)$$

In this notebook we are going to study a very important example of such a system: the **discrete logistic map**. 

At any point if you want to know more about what is going on under the hood, just click the littel eye-like button next to the paragraph/image. This will show the code that is making all of this work.

### The Discrete Logistic System

Let's consider the **discrete logistic** dynamical system as an example 

$$x_{n+1} = \lambda x_n (1 - x_n)$$

It turns out that the discrete case is much more complex than the continuous counterpart! Let's see this by looking at its fixed points and $n$-cycles as we vary the parameter $\lambda$.
To find out where they are you need to find the points that satisfy $f^n(x) = x$, that is those that lie at the intersection between $f^n$ (with $f(x) = \lambda x (1 - x))$ and the line $y = x$.
p.s. If you ever get an error, wait a few seconds to give the notebook the time to sort it all out.

"""

# ╔═╡ 5d29cefe-e277-487d-80c0-ea7bb7309680
md"""
Let's plot $f^n$ then, shall we? Let's begin by defining $f$ in Julia and plotting it.
"""

