# ╔═╡ df01f803-58e7-43d5-801f-66e9233fa046
md"""
Let's now do that for a bunch of initial conditions and see how their positions change with each iteration. The following animation shows the positions with respect to $\lambda$ while the system is evolving (it may take a couple of minutes for the program to run through all the simulations and create the animation).
"""

# ╔═╡ 0159ef5b-3b2a-49df-a519-83dde378cd3f
begin

	lambda_step = 0.01

	#matrix to store all the trajectories for the various values of λ.
	A = Matrix{Float64}[]

	function f_logistic(x, p, t)
    	return p[1] * x[1] * (1 - x[1])
	end
		
	for lambda in 0:lambda_step:4
		
		# save trjactories for a specific λ in a matrix
		Trajs = Matrix{Float64}(undef, 11, 50)
		
		for i in 1:10
			
			# generate random initial states
			s = rand()
			
			# create dynamical system with specified initial state and parameters
			ds = DiscreteDynamicalSystem(f_logistic, s, lambda)
			
			# get and save trajectory
			traj = trajectory(ds, 49)
			Trajs[i, :] = reshape(traj, 1, length(traj))
		end

		# add new trajectories to A
		push!(A, Trajs)
		
	end

	#create animation
	anim = @animate for t = 1:1:50
		
		# intialize plot
		scatter([0], [0], background_color = :black, color = :white, ylims = (0, 1), markersize=1, legend=false)
		
		for lambda in 0:lambda_step:4
			Trajs = A[Int(round(lambda*(1.0/lambda_step)+1))]
			scatter!(fill(lambda, (11)), Trajs[:, t], background_color = :black, color = :white, ylims = (0, 1), markersize=0.5, legend=false)
		end
		
		# give current plot
		current()
	end

	# create animation
	gif(anim, "anim.mp4", fps=10)	
end

